// sampleIncident returns a fully-populated test record. Tests should
// mutate copies of this rather than share the pointer.
func sampleIncident() IncidentRecord {
	return IncidentRecord{
		ID:          "inc-001",
		Severity:    SeveritySEV1,
		Title:       "Test incident",
		Description: "Generated by incident_store_test",
		Status:      IncidentOpen,
		CreatedAt:   time.Date(2026, 7, 5, 12, 0, 0, 0, time.UTC),
		AgentID:     "agent-test",
	}
}

func TestIncidentFileStore_RoundTrip(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "incidents.jsonl")

	store, err := NewIncidentFileStore(path)
	if err != nil {
		t.Fatalf("NewIncidentFileStore: %v", err)
	}
	defer store.Close()

	// Write 3 incidents.
	for i, id := range []string{"inc-001", "inc-002", "inc-003"} {
		rec := sampleIncident()
		rec.ID = id
		rec.Title = "Incident " + id
		rec.Severity = []Severity{SeveritySEV0, SeveritySEV1, SeveritySEV2}[i]
		if err := store.Record(rec); err != nil {
			t.Fatalf("Record %s: %v", id, err)
		}
	}

	// Verify file mode is 0o600 (operator-only read/write).
	info, err := os.Stat(path)
	if err != nil {
		t.Fatalf("stat: %v", err)
	}
	if perm := info.Mode().Perm(); perm != 0o600 {
		t.Errorf("file mode = %o, want 0o600", perm)
	}

	// Replay.
	loaded, err := store.ReplayIncidents()
	if err != nil {
		t.Fatalf("ReplayIncidents: %v", err)
	}
	if len(loaded) != 3 {
		t.Fatalf("got %d records, want 3", len(loaded))
	}
	for i, want := range []string{"inc-001", "inc-002", "inc-003"} {
		if loaded[i].ID != want {
			t.Errorf("record %d: ID = %q, want %q", i, loaded[i].ID, want)
		}
	}
}

