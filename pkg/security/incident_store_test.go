func TestIncidentStore_AppendIsAtomicUnderConcurrency(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "incidents.jsonl")
	store, err := NewIncidentFileStore(path)
	if err != nil {
		t.Fatalf("NewIncidentFileStore: %v", err)
	}
	defer store.Close()

	const n = 50
	var wg sync.WaitGroup
	wg.Add(n)
	for i := 0; i < n; i++ {
		go func(i int) {
			defer wg.Done()
			rec := sampleIncident()
			rec.ID = "inc-conc-" + string(rune('a'+i%26)) + "-" + time.Now().Format("150405.000000000")
			if err := store.Record(rec); err != nil {
				t.Errorf("concurrent Record: %v", err)
			}
		}(i)
	}
	wg.Wait()

	loaded, err := store.ReplayIncidents()
	if err != nil {
		t.Fatalf("ReplayIncidents: %v", err)
	}
	if len(loaded) != n {
		t.Errorf("got %d records, want %d", len(loaded), n)
	}
}

func TestExpandIncidentPath(t *testing.T) {
	home, err := os.UserHomeDir()
	if err != nil {
		t.Skipf("no home dir: %v", err)
	}
	cases := []struct {
		in, want string
	}{
		{"/abs/path", "/abs/path"},
		{"~", home},
		{"~/foo/bar", filepath.Join(home, "foo/bar")},
		{"relative/path", "relative/path"},
	}
	for _, tc := range cases {
		got, err := expandIncidentPath(tc.in)
		if err != nil {
			t.Errorf("expandIncidentPath(%q): %v", tc.in, err)
			continue
		}
		if got != tc.want {
			t.Errorf("expandIncidentPath(%q) = %q, want %q", tc.in, got, tc.want)
		}
	}
}

func TestFormatTimestamp(t *testing.T) {
	if got := FormatTimestamp(time.Time{}); got != "-" {
		t.Errorf("zero time = %q, want %q", got, "-")
	}
	ts := time.Date(2026, 7, 5, 12, 34, 56, 0, time.UTC)
	if got := FormatTimestamp(ts); got != "2026-07-05T12:34:56Z" {
		t.Errorf("formatted = %q, want %q", got, "2026-07-05T12:34:56Z")
	}
}

