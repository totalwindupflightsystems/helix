func TestIncidentWriterStore_NoPath(t *testing.T) {
	var buf bytes.Buffer
	store := NewIncidentWriterStore(&buf)
	if p := store.Path(); p != "" {
		t.Errorf("Path() = %q, want empty", p)
	}
	if err := store.Record(sampleIncident()); err != nil {
		t.Errorf("Record: %v", err)
	}
	if buf.Len() == 0 {
		t.Error("buffer is empty — no JSONL written")
	}
	// Verify the line is valid JSON.
	var got IncidentRecord
	line := bytes.TrimSpace(buf.Bytes())
	if err := json.Unmarshal(line, &got); err != nil {
		t.Fatalf("unmarshal: %v\nline: %s", err, line)
	}
	if got.ID != "inc-001" {
		t.Errorf("ID = %q, want inc-001", got.ID)
	}
}

func TestIncidentStore_RejectsInvalidRecord(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "incidents.jsonl")
	store, err := NewIncidentFileStore(path)
	if err != nil {
		t.Fatalf("NewIncidentFileStore: %v", err)
	}
	defer store.Close()

	cases := []struct {
		name string
		rec  IncidentRecord
	}{
		{"empty ID", IncidentRecord{Severity: SeveritySEV1, Status: IncidentOpen, CreatedAt: time.Now()}},
		{"invalid severity", IncidentRecord{ID: "x", Severity: "BAD", Status: IncidentOpen, CreatedAt: time.Now()}},
		{"empty status", IncidentRecord{ID: "x", Severity: SeveritySEV1, CreatedAt: time.Now()}},
		{"zero CreatedAt", IncidentRecord{ID: "x", Severity: SeveritySEV1, Status: IncidentOpen}},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			if err := store.Record(tc.rec); err == nil {
				t.Error("Record accepted invalid record; want error")
			}
		})
	}
}

func TestIncidentStore_LoadAllSkipsMalformed(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "incidents.jsonl")
	// Pre-populate with one good record + one bad line + a header comment.
	rec := sampleIncident()
	recBytes, _ := json.Marshal(rec)
	contents := append(recBytes, '\n')
	contents = append(contents, []byte("# comment line, ignored\n")...)
	contents = append(contents, []byte("{this is not valid json\n")...)
	contents = append(contents, []byte("\n")...) // blank line
	if err := os.WriteFile(path, contents, 0o600); err != nil {
		t.Fatalf("WriteFile: %v", err)
	}

	store, err := NewIncidentFileStore(path)
	if err != nil {
		t.Fatalf("NewIncidentFileStore: %v", err)
	}
	defer store.Close()

	loaded, err := store.LoadAll()
	if err != nil {
		t.Fatalf("LoadAll: %v", err)
	}
	if len(loaded) != 1 {
		t.Errorf("got %d records, want 1 (malformed lines skipped)", len(loaded))
	}
	if len(loaded) > 0 && loaded[0].ID != "inc-001" {
		t.Errorf("ID = %q, want inc-001", loaded[0].ID)
	}
}

